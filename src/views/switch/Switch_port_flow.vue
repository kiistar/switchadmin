onMounted(() => {
    chartInstance = echarts.init(chartEl.value)
    initChart()
})

onBeforeUnmount(() => {
    if (chartInstance) {
        chartInstance.dispose()
        chartInstance = null
    }
})

// 响应式处理
const setupResizeListener = () => {
    const observer = new ResizeObserver(() => {
        chartInstance?.resize()
    })
    observer.observe(chartEl.value)
}
