// 主入口
const renderChart = (config) => {
    if (!chartEl.value) return
    chartInstance = echarts.init(chartEl.value)
    chartInstance.setOption(config)
}

// 核心逻辑
const initChart = () => {
    if (!chartEl.value) return
    const rawData = apiResponse.data
    const processed = processData(rawData)
    renderChart(buildChartConfig(processed))
    setupResizeListener()
}

const processData = (rawData) => {
    const items = rawData.items
    const stats = {
        in: { values: [], max: 0, avg: 0, p95: 0 },
        out: { values: [], max: 0, avg: 0, p95: 0 }
    }

    // 数据转换和基础统计
    const timeline = items.map((item) => {
        const inValue = convertToBps(item.in_rates)
        const outValue = convertToBps(item.out_rates)

        stats.in.values.push(inValue)
        stats.out.values.push(outValue)
        stats.in.max = Math.max(stats.in.max, inValue)
        stats.out.max = Math.max(stats.out.max, outValue)

        return formatTime(item.timeline_5min)
    })

    // 高级统计计算
    stats.in.avg = calculateAverage(stats.in.values)
    stats.out.avg = calculateAverage(stats.out.values)
    stats.in.p95 = calculatePercentile(stats.in.values, 95)
    stats.out.p95 = calculatePercentile(stats.out.values, 95)

    return {
        timeline,
        series: {
            in: stats.in.values,
            out: stats.out.values
        },
        stats,
        meta: {
            title: `${rawData.switch_name} ${rawData.port_name}`,
            timeRange: `${formatTime(rawData.time_start)} - ${formatTime(rawData.time_end)}`
        }
    }
}

const buildChartConfig = (data) => {
    return {
        title: {
            text: data.meta.title,
            subtext: data.meta.timeRange,
            left: 'center',
            textStyle: { fontSize: 18 }
        },
        legend: [
            {
                data: ['入口流量', '出口流量'],
                orient: 'vertical',
                icon: 'rect',
                textStyle: {
                    fontFamily: 'Consolas,Monaco,monospace'
                },
                formatter: (name) => {
                    const stats = name === '入口流量' ? data.stats.in : data.stats.out
                    return `${name} | 最大:${formatValue(stats.max)}    平均:${formatValue(stats.avg)}    95th:${formatValue(stats.p95)}`
                },
                left: '10%',
                bottom: 25,
                itemGap: 7
            },
            {
                data: [
                    {
                        name: '入口流量'
                    }
                ],
                itemStyle: {
                    color: '#e56d6d'
                },
                icon: 'diamond',
                orient: 'vertical',
                textStyle: {
                    fontFamily: 'Consolas,Monaco,monospace'
                },
                formatter: (name) => {
                    const stats = name === '入口流量' ? data.stats.in : data.stats.out
                    return `入口流量 + 出口流量 95th: ${formatValue(stats.p95)} bps`
                },
                left: '10%',
                bottom: 5,
                itemGap: 7
            }
        ],
        xAxis: {
            type: 'category',
            data: data.timeline,
            // axisLabel: { rotate: 45 },
            axisLine: {
                lineStyle: {
                    width: 1
                }
            }
        },
        yAxis: {
            type: 'value',

            axisLabel: { formatter: formatValue }
        },
        series: [
            createSeries('入口流量', data.series.in, '#30a832', data.stats.in.p95),
            createSeries('出口流量', data.series.out, '#2781de', data.stats.out.p95)
        ],
        tooltip: {
            trigger: 'axis',
            axisPointer: {
                type: 'cross'
            },
            formatter: (params) => {
                let content = `<b>${params[0].axisValue}</b>`
                params.forEach((p) => {
                    content += `<br/>${p.marker} ${p.seriesName}: ${formatValue(p.value)}`
                })
                return content
            }
        },
        grid: { bottom: 120 }
    }
}
// 创建系列配置（包含95th标记）
const createSeries = (name, data, color, p95Value) => {
    return {
        name,
        type: 'line',
        data,
        symbol: 'none',
        lineStyle: { color, width: 1 },
        areaStyle: {
            color: new echarts.graphic.LinearGradient(0, 0, 0, 1, [
                { offset: 0, color: `${color}40` },
                { offset: 1, color: `${color}00` }
            ])
        },
        markLine: {
            silent: true,
            lineStyle: { color: '#d00', type: 'solid', width: 1 },
            data: [{ yAxis: p95Value }],
            label: {
                show: true,
                position: 'end',
                formatter: `${name}\n\n95th: ${formatValue(p95Value)}`,
                color: '#d00',
                backgroundColor: 'rgba(255,255,255,0.8)',
                padding: [4, 8],
                borderRadius: 4,
                distance: 10
            }
        }
    }
}
// 工具方法
const formatValue = (value) => {
    const units = ['', 'K', 'M', 'G']
    let unitIndex = 0
    while (value >= BASE_UNIT && unitIndex < units.length - 1) {
        value /= BASE_UNIT
        unitIndex++
    }
    return `${value.toFixed(2)} ${units[unitIndex]}bps`
}

const formatTime = (timestamp) => {
    return dayjs(timestamp * 1000)
        .utc()
        .format(TIME_FORMAT) // 明确使用UTC时间
}
const convertToBps = (byteValue) => {
    return byteValue * 8 // 字节转比特
}
const calculateAverage = (values) => {
    return values.reduce((sum, v) => sum + v, 0) / values.length
}
const calculatePercentile = (values, percentile) => {
    const sorted = [...values].sort((a, b) => a - b)
    const index = Math.ceil((sorted.length * percentile) / 100) - 1
    return sorted[index] || 0
}

